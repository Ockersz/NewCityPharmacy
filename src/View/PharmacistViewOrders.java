    public PharmacistViewOrders(int orderId) {
        initComponents();
        
        int x = 0;
        int y = 10;
        
        List<ProductPriceDetails> ProductList = new ArrayList<>();
        
        OrderController orderControl = new OrderController();
        ProductList = orderControl.getOrderProducts(orderId);
        QuotationUI QU = new QuotationUI();
        
        Date date = orderControl.getDate(orderId);
        String strDate = null;
        
                
        
        SimpleDateFormat simple = new SimpleDateFormat("dd-MM-yyyy");
        strDate = simple.format(date);
        
        this.setBorder(javax.swing.BorderFactory.createEmptyBorder(0, 0, 0, 0));
        BasicInternalFrameUI bi = (BasicInternalFrameUI) this.getUI();
        bi.setNorthPane(null);

        

        for (ProductPriceDetails list : ProductList) {

            String generic = list.getGeneric();
            String dosage = list.getDosage();
            int quantity = list.getQuantity();
            int amount = list.getAmount();
            QU.createEntry(jPanel2, x, y, generic, dosage, quantity, amount);

            y = y + 80;
            if (y > jPanel2.getHeight()) {
                jPanel2.setPreferredSize(new Dimension(1075, y + 100));
            }

        }
        lbl_customerName.setText(orderControl.getCustomerName(orderId));
        lbl_orderDate.setText(strDate);
        lbl_orderId.setText(Integer.toString(orderId));
        lbl_orderStatus.setText(orderControl.getStatus(orderId));
        lbl_shippingAddress.setText(orderControl.getCustomerAddress(orderId));
        lbl_customerTp.setText(Integer.toString(orderControl.getCustomerTelephone(orderId)));
        lbl_quotationId.setText(Integer.toString(orderControl.getQuotationId(orderId)));
        lbl_totalAmount.setText(Integer.toString(orderControl.getAmount(orderId))+".00");

    }

