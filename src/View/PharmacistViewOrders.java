    private void btn_shipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_shipActionPerformed
        OrderController obj = new OrderController();
        if(obj.shipOrder(Integer.parseInt(lbl_orderId.getText()))){
            JOptionPane.showMessageDialog(null, "The order has been shipped." , "Success",JOptionPane.INFORMATION_MESSAGE);
            PharmacistOrderList list = new PharmacistOrderList();
            PharmacistDashboard.sendLoader(list);
        }
        else{
            JOptionPane.showMessageDialog(null, "Something went wrong. Please try again." , "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_shipActionPerformed

