     public void loadQuotation(int quotationId) {
        int x = 0;
        int y = 0;
        

        QuotationController QC = new QuotationController();
         P = QC.getQuotationProductDetails(quotationId);
        QuotationUI QU = new QuotationUI();
        for (ProductPriceDetails list : P) {
            
            String generic = list.getGeneric();
            String dosage = list.getDosage();
            int quantity = list.getQuantity();
            int amount = list.getAmount();
            total += amount;
            QU.createEntry(jPanel2, x, y, generic, dosage, quantity, amount);

            y = y + 80;

            if (y > jPanel2.getHeight()) {
                jPanel2.setPreferredSize(new Dimension(1075, y + 100));
            }
        }

        lbl_quotationId.setText(Integer.toString(quotationId));
        lbl_prescriptionId.setText(Integer.toString(QC.getPrescriptionId(quotationId)));
        lbl_totalAmount.setText(Integer.toString(total));
        

    }

