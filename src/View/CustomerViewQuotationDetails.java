    private void btn_confirmOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmOrderActionPerformed
        OrderController orderController = new OrderController();
        QuotationController quotationController = new QuotationController();
        if(orderController.confirmOrder(total, quotationId, CustomerDashboard.customerId, P))
        {
            JOptionPane.showMessageDialog(null, "Your order has been confirmed and an email is sent to you. Thank you." , "Order placed", JOptionPane.INFORMATION_MESSAGE);
            CustomerMyOrders back = new CustomerMyOrders();
            CustomerDashboard.SendLoader(back);
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Order cannot be placed" , "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_confirmOrderActionPerformed

    private void btn_cancelOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cancelOrderActionPerformed
        QuotationController obj = new QuotationController();
        if(obj.cancelQuotation(quotationId)){
            JOptionPane.showMessageDialog(null, "Quotation has been canceled", "Success", JOptionPane.INFORMATION_MESSAGE);
            CustomerMyOrders back = new CustomerMyOrders();
            CustomerDashboard.SendLoader(back);
        }
        else{
            JOptionPane.showMessageDialog(null, "Quotation cannot be canceled", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_cancelOrderActionPerformed


